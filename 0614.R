#(1)SI放专缂

fTd <- function(T,RH){
  es <- 0.611*exp((17.5*T)/(240.97+T))
  ea <- es*RH/100
  Td <- (240.97*log(ea/0.611))/(17.5-log(ea/0.611))
  return(Td)
}

